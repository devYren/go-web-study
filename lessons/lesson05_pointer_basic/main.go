package main

import "fmt"

// ==========================================
// 指针的本质：变量的内存地址
// ==========================================
// Java 程序员其实天天在用指针，只是 Java 帮你隐藏了。
// Go 把它暴露出来了，但只有两个符号需要记：
//   & → 取地址（"这个变量在内存哪里？"）
//   * → 两个含义：
//       1. 类型前面：指针类型（*int = 指向 int 的指针）
//       2. 变量前面：解引用（"根据地址找到实际的值"）

func main() {
	// ==========================================
	// 1. 基本用法
	// ==========================================
	x := 42

	p := &x // p 存的是 x 的内存地址，p 的类型是 *int
	//  &x = 取 x 的地址

	fmt.Println("x 的值:", x)    // 42
	fmt.Println("x 的地址:", p)   // 0xc0000b2008（某个内存地址）
	fmt.Println("p 指向的值:", *p) // 42（通过地址找到值）
	//                          *p = 解引用：根据地址取出值

	// 通过指针修改原始值
	*p = 100
	fmt.Println("修改后 x:", x) // 100！x 被改了

	// ==========================================
	// 2. 用 Java 类比理解
	// ==========================================
	//
	// Java（所有对象都是引用，你看不到地址）：
	//   User u = new User();    // u 存的其实就是地址，但 Java 不让你看
	//   u.name = "Alice";       // Java 自动通过地址找到对象，修改字段
	//
	// Go（手动控制）：
	//   u := User{}             // u 存的是值本身（不是地址！）
	//   p := &u                 // 手动取地址
	//   p.Name = "Alice"        // 通过地址修改（Go 自动解引用，不用写 (*p).Name）

	// ==========================================
	// 3. 值传递 vs 指针传递
	// ==========================================

	a := 10
	addOne(a)
	fmt.Println("addOne 后 a:", a) // 还是 10，没变！

	addOneByPointer(&a)
	fmt.Println("addOneByPointer 后 a:", a) // 11，变了！
}

// 值传递：函数拿到的是副本，改了影响不到外面
// Java 的基本类型（int, double）也是这样
func addOne(n int) {
	n = n + 1 // 改的是副本
}

// 指针传递：函数拿到的是地址，通过地址修改原始值
// Java 的对象传递类似这个效果
func addOneByPointer(n *int) {
	*n = *n + 1 // *n 解引用，修改的是原始值
}
